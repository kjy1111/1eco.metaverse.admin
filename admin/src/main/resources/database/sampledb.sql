CREATE MEMORY TABLE EXCELSAMPLE(pid INT NOT NULL PRIMARY KEY,name VARCHAR(50),email VARCHAR(100),cellphone VARCHAR(100));
CREATE MEMORY TABLE FileInfo(
    FilePid INT NOT NULL PRIMARY KEY,
    RealName VARCHAR(500),
    VirtualName VARCHAR(500),
    ContentType VARCHAR(100) ,
    Category VARCHAR(128) ,
    Size int ,
    Height INT ,
    Width INT ,
    ThumbnailYN char(1),
    DeletedYN char(1),
    CreatedDatetime DATETIME
);

CREATE MEMORY TABLE EventLog (
	EventLogPid int NOT NULL,
	UserId VARCHAR (64),
	EventCode VARCHAR(64) NOT NULL,
	EventName VARCHAR(256) NOT NULL,
	EventType VARCHAR(256) NOT NULL,
	Message VARCHAR(500) NOT NULL,
	CreatedDatetime DATETIME NOT NULL
)

CREATE MEMORY TABLE UserInfo(
    UserPid INT NOT NULL PRIMARY KEY,
    Userid VARCHAR(50),
    Password VARCHAR(100),
    UserName VARCHAR(100),
    RememberMeYN  varchar(100))

CREATE MEMORY TABLE UserLogInfo(
	UserLogPid int NOT NULL PRIMARY KEY,
	Userid varchar(64),
	IpAdres varchar(64),
	ActionNm varchar(64),
	FormData varchar(8000),
	CreatDt varchar(50))

